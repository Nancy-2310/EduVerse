/**
 * @REGISTER
 * @ROUTE @POST {{URL}}/api/v1/user/register
 * @ACCESS Public
 */
export const registerUser = asyncHandler(async (req, res, next) => {
  try {
    console.log('=== REGISTRATION DEBUG ===');
    console.log('Request body:', req.body);
    console.log('Request file:', req.file);
    
    const { fullName, email, password } = req.body;

    // Validation
    if (!fullName || !email || !password) {
      console.log('Missing required fields');
      return next(new AppError('All fields are required', 400));
    }

    // Check if user exists
    console.log('Checking if user exists with email:', email);
    const userExists = await User.findOne({ email });

    if (userExists) {
      console.log('User already exists');
      return next(new AppError('Email already exists', 409));
    }

    console.log('Creating new user...');
    
    // Create user with default avatar first
    const user = await User.create({
      fullName,
      email,
      password,
      avatar: {
        public_id: email,
        secure_url: 'https://res.cloudinary.com/du9jzqlpt/image/upload/v1674647316/avatar_drzgxv.jpg',
      },
    });

    console.log('User created successfully');

    // Handle file upload asynchronously (don't block the response)
    if (req.file) {
      console.log('Processing uploaded file:', req.file.filename);
      
      // Upload to Cloudinary with timeout and better error handling
      cloudinary.v2.uploader.upload(req.file.path, {
        folder: 'lms',
        width: 250,
        height: 250,
        gravity: 'faces',
        crop: 'fill',
        timeout: 60000 // 60 second timeout
      }).then(result => {
        if (result) {
          console.log('Cloudinary upload successful:', result.public_id);
          // Update user avatar in background
          User.findByIdAndUpdate(user._id, {
            'avatar.public_id': result.public_id,
            'avatar.secure_url': result.secure_url
          }).exec();
        }
        
        // Clean up local file
         fs.rm(`uploads/${req.file.filename}`)
         .catch(err => console.error('Error removing file:', err));


      }).catch(error => {
        console.error('Cloudinary upload error (background):', error);
        // Clean up local file even on error
        fs.rm(`uploads/${req.file.filename}`, (err) => {
          if (err) console.error('Error removing file:', err);
        });
      });
    }

    // Generate JWT token
    const token = await user.generateJWTToken();
    console.log('JWT token generated',token);

    // Prepare response data (without password)
    const responseUser = {
      _id: user._id,
      fullName: user.fullName,
      email: user.email,
      avatar: user.avatar,
      role: user.role,
      subscription: user.subscription,
      createdAt: user.createdAt,
      updatedAt: user.updatedAt
    };

    const responseData = {
      success: true,
      message: 'User registered successfully',
      user: responseUser,
    };

    // Set cookie with proper options
    const cookieOptions = {
      maxAge: 7 * 24 * 60 * 60 * 1000, // 7 days
      httpOnly: true,
      secure: process.env.NODE_ENV === 'production',
      sameSite: 'lax'
    };

    res.cookie('token', token, cookieOptions);

    console.log('=== REGISTRATION SUCCESS ===');
    console.log('Sending response:', JSON.stringify(responseData, null, 2));

    // Set explicit headers and send response
    res.setHeader('Content-Type', 'application/json');
    return res.status(201).json(responseData);

  } catch (error) {
    console.error('=== REGISTRATION ERROR ===');
    console.error('Error details:', error);
    
    // Ensure we don't send multiple responses
    if (res.headersSent) {
      console.error('Headers already sent, cannot send error response');
      return;
    }
    
    return next(new AppError(error.message || 'Internal server error', 500));
  }
});
/**
 * @LOGIN
 * @ROUTE @POST {{URL}}/api/v1/user/login
 * @ACCESS Public
 */
export const loginUser = asyncHandler(async (req, res, next) => {
  // Destructuring the necessary data from req object
  const { email, password } = req.body;

  // Check if the data is there or not, if not throw error message
  if (!email || !password) {
    return next(new AppError('Email and Password are required', 400));
  }

  // Finding the user with the sent email
  const user = await User.findOne({ email }).select('+password');

  // If no user or sent password do not match then send generic response
  if (!(user && (await user.comparePassword(password)))) {
    return next(
      new AppError('Email or Password do not match or user does not exist', 401)
    );
  }

  // Generating a JWT token
  const token = await user.generateJWTToken();
  console.log('JWT token generated for login:', token);
  // Setting the password to undefined so it does not get sent in the response
  user.password = undefined;

  // Setting the token in the cookie with name token along with cookieOptions
  res.cookie('token', token, cookieOptions);

  // If all good send the response to the frontend
  res.status(200).json({
    success: true,
    message: 'User logged in successfully',
    user,
  });
});

